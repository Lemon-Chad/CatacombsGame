package com.lemon.catacombs.items;

public class MeleeRange {
    private final double width;
    private final double height;

    public MeleeRange(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }
}
